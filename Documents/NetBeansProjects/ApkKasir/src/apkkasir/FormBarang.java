    private void dataTabel(){
        dataBarang.setVisible(false);
        tambahBarang.setVisible(true);
        
        int row = tbl_Produk.getSelectedRow();
        jLabel2.setText("Perbarui Data Produk");
        
        text_IdProduk.setEnabled(false);
        
        text_IdProduk.setText       (tbl_Produk.getModel().getValueAt(row, 1).toString());
        text_NamaProduk.setText     (tbl_Produk.getModel().getValueAt(row, 2).toString());
        text_HargaBeli.setText      (tbl_Produk.getModel().getValueAt(row, 3).toString());
        text_HargaJual.setText      (tbl_Produk.getModel().getValueAt(row, 4).toString());
        text_Stok.setText           (tbl_Produk.getModel().getValueAt(row, 5).toString());
        combo_Satuan.setSelectedItem(tbl_Produk.getModel().getValueAt(row, 6).toString());
        
        aktif();
        bt_Simpan.setText("PERBARUI");
        bt_Batal.setVisible(true);
    }
    private void aktif(){
        text_NamaProduk.setEnabled(true);
        text_HargaBeli.setEnabled(true);
        text_HargaJual.setEnabled(true);
        text_Stok.setEnabled(true);
        combo_Satuan.setEnabled(true);
        
    }
    private void loadData() {
        bt_Hapus.setVisible(false);
        bt_Batal.setVisible(false);
        List<model_Produk> list = servis.getData();
        tblModel.setData(list);
    }
    private void hapusData(){
        int index = tbl_Produk.getSelectedRow();
        if (index!=-1) {
            model_Produk brg = tblModel.getData(tbl_Produk.convertRowIndexToModel(index));
            if (JOptionPane.showConfirmDialog(null, "Yakin data akan di hapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION)== JOptionPane.OK_OPTION) {
                servis.hapusData(brg);
                tblModel.hapusData(index);
                loadData();
                resetForm();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan di update");
        }
    }

    private void simpanData() {
        if(validasiInput()==true){
            int id_produk         = Integer.parseInt(text_IdProduk.getText());
            String nama_produk    = text_NamaProduk.getText();
            Long harga_beli       = Long.parseLong(text_HargaBeli.getText());
            Long harga_jual       = Long.parseLong(text_HargaJual.getText());
            int stok              = Integer.parseInt(text_Stok.getText());
            String satuan         = combo_Satuan.getSelectedItem().toString();
            
            model_Produk brg = new model_Produk();
            
            brg.setId_produk(id_produk);
            brg.setNama_produk(nama_produk);
            brg.setHarga_beli(harga_beli);
            brg.setHarga_jual(harga_jual);
            brg.setStok(stok);
            brg.setSatuan(satuan);
            
            servis.tambahData(brg);
            tblModel.tambahData(brg);
            loadData();
            resetForm();
            tampilPanel();
            bt_Simpan.setText("TAMBAH");
        }
    }

    private void perbaruiData() {
        int index = tbl_Produk.getSelectedRow();
        if (index!=-1) {
            model_Produk mobar = tblModel.getData(tbl_Produk.convertRowIndexToModel(index));
            
            if(validasiInput()==true){
                int id_produk = Integer.parseInt(text_IdProduk.getText());
                String nama_produk    = text_NamaProduk.getText();
                Long harga_beli       = Long.parseLong(text_HargaBeli.getText());
                Long harga_jual       = Long.parseLong(text_HargaJual.getText());
                int stok              = Integer.parseInt(text_Stok.getText());
                String satuan         = combo_Satuan.getSelectedItem().toString();
                
                model_Produk brg = new model_Produk();
                
                brg.setId_produk(id_produk);
                brg.setNama_produk(nama_produk);
                brg.setHarga_beli(harga_beli);
                brg.setHarga_jual(harga_jual);
                brg.setStok(stok);
                brg.setSatuan(satuan);
                
                servis.perbaruiData(brg);
                tblModel.perbaruiData(index, brg);
                loadData();
                resetForm();
                tampilPanel();
            }
        }
        
    }

